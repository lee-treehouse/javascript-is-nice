/*

You are given two strings s and t.

String t is generated by random shuffling string s and then add one more letter at a random position.

Return the letter that was added to t.

Here is a solution similar to mine except only one hashmap is used to reduce space complexity 
https://leetcode.com/problems/find-the-difference/solutions/2780619/js-solution-with-object-very-simple-very-understand/
*/
/**
 * @param {string} s
 * @param {string} t
 * @return {character}
 */
var findTheDifference = function (s, t) {
  let letterCount = {};
  for (let i = 0; i < s.length; i++) {
    const letter = s[i];
    if (letterCount[letter]) {
      letterCount[letter]++;
    } else {
      letterCount[letter] = 1;
    }
  }

  let tLetterCount = {};
  for (let i = 0; i < t.length; i++) {
    const letter = t[i];
    if (tLetterCount[letter]) {
      tLetterCount[letter]++;
    } else {
      tLetterCount[letter] = 1;
    }
    if (!letterCount[letter] || letterCount[letter] < tLetterCount[letter]) {
      return letter;
    }
  }
  return "not found";
};

/*

Input: s = "abcd", t = "abcde"
Output: "e"

*/
console.log(findTheDifference("abcd", "abcde"));
console.log(findTheDifference("", "y"));
